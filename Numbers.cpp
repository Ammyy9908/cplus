#include <iostream>
#include <cmath>
using namespace std;

int main(){
    int num1 = 10;
    double num2 = 15.5;
    cout << num1 << endl;
    cout << num2 << endl;
    num1++;
    cout << num1 << endl;
    
    num2+=1;
    cout << num2 << endl;
    
    // math operations
    
    cout << pow(2,5) << endl;
    
    cout << sqrt(16) << endl;
    
    cout << round(4.5) << endl;
    
    cout << floor(4.8) << endl;
    
    cout << fmax(3,10) << endl;
    
    cout << fmin(3,9) << endl;
    return 0;
}
