#include <iostream>
using namespace std;

int main(){
    
    // char
    char grade = 'A';
    cout << grade << endl;
    
    // string
    
    string name = "Sumit Bighaniya";
    
    cout << name << endl;
    
    // numbers
    //whole number or integers
    int age = 25;
    cout << age << endl;
    
    // floating numbers having decimal place
    
    double cgpa = 7.0;
    
    cout << cgpa << endl;
    
    // bool
    
    bool isRainy = false;
    
    if(isRainy){
        cout << "Today is Rainy Day" << endl;
    }
    else{
        cout << "It is sunny day" << endl;
    }
    
    
    
    return 0;
}
